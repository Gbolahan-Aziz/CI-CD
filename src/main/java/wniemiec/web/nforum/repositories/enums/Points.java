package wniemiec.web.nforum.repositories.enums;


/**
 * Responsible for representing points that a user can receive.
 */
public enum Points {
	
	//-------------------------------------------------------------------------
	//		Enumerations
	//-------------------------------------------------------------------------
	NEW_TOPIC(10),
	NEW_COMMENT(3);


	//-------------------------------------------------------------------------
	//		Attributes
	//-------------------------------------------------------------------------
	private int value;
	

	//-------------------------------------------------------------------------
	//		Constructor
	//-------------------------------------------------------------------------
	Points(int value) {
		this.value = value;
	}
	

	//-------------------------------------------------------------------------
	//		Getters
	//-------------------------------------------------------------------------
	public int getValue() {
		return value;
	}
}
